#include "3-calc.h"

/**
  * op_add - add two int
  *@a: int a
  *@b: int b
  *
  * Return: the sum
  **/
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - sub two int
  *@a: int a
  *@b: int b
  *
  * Return: the result
  **/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - mul two int
  *@a: int a
  *@b: int b
  *
  * Return: the result
  **/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - div two int
  *@a: int a
  *@b: int b
  *
  * Return: the result
  **/
int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - mod two int
  *@a: int a
  *@b: int b
  *
  * Return: the result
  **/
int op_mod(int a, int b)
{
	return (a % b);
}
